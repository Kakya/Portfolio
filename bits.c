/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
	/*
		My Xor solution will be of the following form:
		(X'Y')'(XY)' == (X+Y)(X'+Y'), which is the definition of Xor
	*/
	int negX = ~x;
	int negY = ~y;
	int firstPart = ~(negX & negY);
	int secondPart = ~(x & y);
	int result = firstPart & secondPart;
  return result;
}
