/* 
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
  //shift LSB to front and then perform an arithmetic shift filling in for the LSB
  int shift = x << 31;
  int result = shift >> 31;
  return result;
}
